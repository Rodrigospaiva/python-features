# POP( ) retira do final da pilha. (pode ou não colocar índice)
# REMOVE( ) retira um elemento específico.

lista_animais = ['cachorro', 'gato', 'coelho', 'tartaruga']
print(lista_animais)

lista_animais.pop()

print('\nAnimal retirado.')
print('Nova lista: ', '\n', lista_animais, '\n')

print('Fim do programa.')