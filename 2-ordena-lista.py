# SORT( ) ordena crescente, REVERT( ) ordena reversamente.

numeros = [10, 15, 28, 4, 17, 26, 1, 3, 39]
animais = ['cachorro', 'gato', 'coelho',  'tartaruga']

print(numeros)
print(animais)

numeros.sort()
animais.sort()

print("\nNúmeros da lista em ordem crescente: ", numeros)
print("Animais da lista em ordem alfabética: ", animais)